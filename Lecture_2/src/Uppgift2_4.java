public class Uppgift2_4 {
    public static void main(String[] args)
    {
        int tal1 = 10;
        int tal2 = 5;
        int tal3 = (tal1 + tal2) * 2;

        if (tal3 >= 20)
        {
            System.out.println("Det är sant");
        }
        else  if (tal3 < 20)
        {
            System.out.println("Talet är inte större");
        }
    }
}